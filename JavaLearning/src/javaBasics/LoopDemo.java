package javaBasics;

public class LoopDemo {
	
	public static void main(String[] args) {
		
		
		//for loop
		
		System.out.println("Loop started");
		
		for(int i=1;i<=20;i++)
		{
			System.out.println("Value is " +i);
		
	    }
		System.out.println("Loop ended");
	
	//while loop
	int count=10;
	
	while(count<20)
	{
		System.out.println("Count is "+count);
		
		count++;
	}
}

}